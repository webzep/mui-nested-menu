import { css } from '@emotion/react';
import styled from '@emotion/styled';
import { FC, useEffect } from 'react';

import { PaletteConfig } from '../core/themes/palette';

type MaterialIconCssProps = {
    fontColor?: string;
    fontSize?: string;
    themeFontColor?: keyof PaletteConfig;
};

const IconContainer = styled.span<MaterialIconCssProps>`
    ${({ fontColor, fontSize, theme, themeFontColor }) => {
        const size = fontSize ? `font-size: ${fontSize} !important;` : '';
        const color = fontColor ?? theme.palette[themeFontColor as keyof PaletteConfig] ?? 'inherit';

        return css`
            color: ${color} !important;
            ${size};
            /* pointer-events: none; */
        `;
    }}
`;

type MaterialIconProps = MaterialIconCssProps & {
    className?: string; // Allows emotion to pass the className prop
    icon: IconNames;
    sharp?: boolean;
};

export const MaterialIcon: FC<MaterialIconProps> = ({ icon, sharp, ...props }) => {
    useEffect(() => {
        const linkHref = `https://fonts.googleapis.com/icon?family=Material+Icons${sharp ? '' : '+Round'}`;
        if (!document.querySelector(`link[href="${linkHref}"]`)) {
            const link = document.createElement('link');
            link.href = linkHref;
            link.rel = 'stylesheet';
            document.head.appendChild(link);
        }
    }, []);

    return (
        <IconContainer
            {...props}
            className={`material-icons${sharp ? '' : '-round'} ${props.className ?? ''}`}
        >
            {icon}
        </IconContainer>
    );
};

